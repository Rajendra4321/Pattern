public class Pattern5 {
   public static void main(String[] args) {
	
	   int star=1;
	   int count=4;
	   
	   for (int i = 0; i < count; i++) {
		   
		   for (int j = 0; j < star; j++) {
			   
			   System.out.print(" * ");
			   
			
		}
		   System.out.println("");
		   star=star+2;
		
	}
}
}
